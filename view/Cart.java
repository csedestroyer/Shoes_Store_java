    private void buttonDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonDeleteActionPerformed
        // TODO add your handling code here:
        if(this.buyProduk != null){
            new ControllerCart(this.home, this).deleteCart(this.buyProduk.getIdcart());
        }else{
            JOptionPane.showMessageDialog(this, "Pilih salah satu produk");
        }
    }//GEN-LAST:event_buttonDeleteActionPerformed

    private void buttonBuyActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonBuyActionPerformed
        // TODO add your handling code here:
        if(this.buyProduk != null){
            if(Integer.valueOf(labelQty.getText()) > this.buyProduk.getSize().getStok()){
                JOptionPane.showMessageDialog(this, "Stok beli melebih kuota stok produk");
            }else{
                new ControllerCart(this.home, this).buyCart(this.buyProduk, Integer.valueOf(labelQty.getText()));
            }
        }else{
            JOptionPane.showMessageDialog(this, "Pilih salah satu produk");
        }
    }//GEN-LAST:event_buttonBuyActionPerformed

