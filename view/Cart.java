    public void setBuyText(int qty){
        new ControllerCart(this.home, this).showCart(panelCart);
        
        if(this.buyProduk == null){
            labelHarga.setVisible(false);
            labelNama.setVisible(false);
            labelQty.setVisible(false);
            labelSize.setVisible(false);
            labelTotalHarga.setText("0");
        }else{
            labelHarga.setVisible(true);
            labelNama.setVisible(true);
            labelQty.setVisible(true);
            labelSize.setVisible(true);
            
            Locale indo = new Locale("in", "ID");
            NumberFormat formatRp = NumberFormat.getCurrencyInstance(indo);
            int totalHarga = this.buyProduk.getProduk().getHarga() * qty;
            
            labelHarga.setText(String.valueOf(this.buyProduk.getProduk().getHarga()));
            labelNama.setText(this.buyProduk.getProduk().getNamaproduk());
            labelQty.setText(String.valueOf(qty));
            labelSize.setText(String.valueOf(this.buyProduk.getSize().getSize()));
            labelTotalHarga.setText(formatRp.format(totalHarga));
        }
    }

